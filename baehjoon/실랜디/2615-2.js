const graph = input.map((line) => line.split(" ").map(Number));

const dx = [1, 0, 1, 1],
  dy = [0, 1, 1, -1];

let ans = 0;
let location = "";

const dfs = (y, x, k, cnt) => {
  const nx = x + dx[k];
  const ny = y + dy[k];

  if (cnt === 5) {
    if (nx < 0 || ny < 0 || nx >= 19 || ny >= 19) return graph[y][x];
    return graph[ny][nx] === graph[y][x] ? 0 : graph[y][x];
  }

  if (nx < 0 || ny < 0 || nx >= 19 || ny >= 19) return 0;

  if (graph[ny][nx] === graph[y][x]) {
    return dfs(ny, nx, k, cnt + 1);
  }

  return 0;
};

outer: for (let i = 0; i < 19; i++) {
  for (let j = 0; j < 19; j++) {
    if (graph[i][j]) {
      for (let k = 0; k < 4; k++) {
        if (dfs(i, j, k, 1)) {
          const px = j - dx[k];
          const py = i - dy[k];
          if (px < 0 || py < 0 || px >= 19 || py >= 19) {
            ans = graph[i][j];
            location = [i + 1, j + 1];
            break outer;
          }

          if (graph[i][j] !== graph[py][px]) {
            ans = graph[i][j];
            location = [i + 1, j + 1];
            break outer;
          }
        }
      }
    }
  }
}

